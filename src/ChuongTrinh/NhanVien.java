
package ChuongTrinh;


public class NhanVien {
    public  String MaNV;
    public String hoTen;
    public String namSinh;
    public String email;
    public String sodt;
    public String luong;
    public String gioiTinh;
    public String diaChi;

    public NhanVien() {
    }

    public NhanVien(String MaNV, String hoTen, String namSinh, String email, String sodt, String luong, String gioiTinh, String diaChi) {
        this.MaNV = MaNV;
        this.hoTen = hoTen;
        this.namSinh = namSinh;
        this.email = email;
        this.sodt = sodt;
        this.luong = luong;
        this.gioiTinh = gioiTinh;
        this.diaChi = diaChi;
    }
    
}
