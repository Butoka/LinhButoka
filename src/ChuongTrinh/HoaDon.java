
package ChuongTrinh;

public class HoaDon {
    public String MaHD;
    public String MaNV;
    public String MaSP;
    public String tenSP;
    public String SoLuong;
    public String donGia;
    public String thanhTien;

    public HoaDon() {
    }

    public HoaDon(String MaHD, String MaNV, String MaSP,String tenSP, String SoLuong, String donGia, String thanhTien) {
        this.MaHD = MaHD;
        this.MaNV = MaNV;
        this.MaSP = MaSP;
        this.tenSP = tenSP;
        this.SoLuong = SoLuong;
        this.donGia = donGia;
        this.thanhTien = thanhTien;
    }
}
